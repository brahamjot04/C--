// Demonstrate the use of Data Types

#include<iostream>
using namespace std;

int main()
{
    int a = 10;
    float b = 10.5;
    char c = 'a';
    string d = "Hello World!";
    double e = 10;
    bool f = false;
    cout << a << "\n" << b << "\n" << c << "\n" << d << "\n" << e << "\n" << f << endl;
    return 0;
}